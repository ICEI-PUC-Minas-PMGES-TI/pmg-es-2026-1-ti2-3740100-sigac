async function request(method, route, token, body) {
  const headers = { Accept: 'application/json' };
  if (body !== undefined) {
    headers['Content-Type'] = 'application/json';
  }
  if (token) {
    headers.Authorization = `Bearer ${token}`;
  }

  let response;
  try {
    response = await fetch(`${BASE_URL}${route}`, {
      method,
      headers,
      body: body !== undefined ? JSON.stringify(body) : undefined,
      signal: AbortSignal.timeout(20000),
    });
  } catch (error) {
    throw new Error(`Falha de conexão em ${method} ${route}: ${error.message}`);
  }

  const raw = await response.text();
  let parsed = null;
  if (raw) {
    try {
      parsed = JSON.parse(raw);
    } catch {
      parsed = raw;
    }
  }

  if (!response.ok) {
    const detail = typeof parsed === 'string' ? parsed : JSON.stringify(parsed);
    throw new Error(`Erro HTTP ${response.status} em ${method} ${route}: ${detail}`);
  }

  return parsed;
}

async function login(email, password) {
  return request('POST', '/auth/login', null, { email, password });
}

async function seedCondominio(adminToken, entry) {
  const condominio = await request('POST', '/condominios', adminToken, {
    nome: entry.nome,
    endereco: entry.endereco,
    cnpj: entry.cnpj,
  });

  await request('POST', `/condominios/${condominio.id}/gestores`, adminToken, {
    nome: entry.gestor.nome,
    email: entry.gestor.email,
    password: entry.gestor.password,
    role: 'GESTOR',
  });

  await request('POST', `/condominios/${condominio.id}/sindicos`, adminToken, {
    nome: entry.sindico.nome,
    email: entry.sindico.email,
    password: entry.sindico.password,
    role: 'SINDICO',
  });

  const gestorAuth = await login(entry.gestor.email, entry.gestor.password);
  const sindicoAuth = await login(entry.sindico.email, entry.sindico.password);

  const inquilinos = [];
  for (const tenant of entry.inquilinos) {
    const created = await request('POST', `/condominios/${condominio.id}/inquilinos`, gestorAuth.token, tenant);
    inquilinos.push(created);
  }

  for (const employee of entry.funcionarios) {
    await request('POST', `/condominios/${condominio.id}/funcionarios`, gestorAuth.token, employee);
  }

  for (const arrecadacao of entry.arrecadacoes) {
    await request('PUT', `/condominios/${condominio.id}/arrecadacao-mensal`, gestorAuth.token, arrecadacao);
  }

  for (const gasto of entry.gastos) {
    await request('POST', `/condominios/${condominio.id}/gastos-produto`, gestorAuth.token, gasto);
  }

  const solicitacoesCriadas = [];
  for (const titulo of entry.solicitacoes) {
    const created = await request(
      'POST',
      `/condominios/${condominio.id}/solicitacoes-manutencao`,
      sindicoAuth.token,
      { titulo },
    );
    solicitacoesCriadas.push(created);
  }

  for (let index = 0; index < entry.manutencoes.length; index += 1) {
    const manutencao = { ...entry.manutencoes[index] };
    if (index === 0 && solicitacoesCriadas[0]) {
      manutencao.solicitacaoId = solicitacoesCriadas[0].id;
    }
    await request('POST', `/condominios/${condominio.id}/manutencoes`, gestorAuth.token, manutencao);
  }

  for (const aviso of entry.avisos) {
    const payload = {
      titulo: aviso.titulo,
      mensagem: aviso.mensagem,
      dataReferencia: aviso.dataReferencia,
      alcance: aviso.alcance,
    };

    if (aviso.alcance === 'ESPECIFICOS') {
      payload.destinatarios = aviso.destinatarios.map((nome) => {
        const tenant = inquilinos.find((item) => item.nome === nome);
        if (!tenant) {
          throw new Error(`Inquilino destinatário não encontrado para aviso específico: ${nome}`);
        }
        return {
          inquilinoId: tenant.id,
          nome: tenant.nome,
          email: tenant.email,
        };
      });
    }

    await request('POST', `/condominios/${condominio.id}/avisos`, gestorAuth.token, payload);
  }

  return {
    condominio,
    gestor: entry.gestor,
    sindico: entry.sindico,
    inquilinos: inquilinos.length,
    funcionarios: entry.funcionarios.length,
    gastos: entry.gastos.length,
    manutencoes: entry.manutencoes.length,
    avisos: entry.avisos.length,
    arrecadacoes: entry.arrecadacoes.length,
    solicitacoesPendentes: Math.max(solicitacoesCriadas.length - 1, 0),
  };
}

function printSummary(results) {
  const totals = results.reduce(
    (accumulator, item) => ({
      condominios: accumulator.condominios + 1,
      inquilinos: accumulator.inquilinos + item.inquilinos,
      funcionarios: accumulator.funcionarios + item.funcionarios,
      gastos: accumulator.gastos + item.gastos,
      manutencoes: accumulator.manutencoes + item.manutencoes,
      avisos: accumulator.avisos + item.avisos,
      arrecadacoes: accumulator.arrecadacoes + item.arrecadacoes,
      solicitacoesPendentes: accumulator.solicitacoesPendentes + item.solicitacoesPendentes,
    }),
    {
      condominios: 0,
      inquilinos: 0,
      funcionarios: 0,
      gastos: 0,
      manutencoes: 0,
      avisos: 0,
      arrecadacoes: 0,
      solicitacoesPendentes: 0,
    },
  );

  log('');
  log('Seed concluído com sucesso.');
  log(`Base HTTP: ${BASE_URL}`);
  log(`Condomínios criados: ${totals.condominios}`);
  log(`Gestores criados: ${totals.condominios}`);
  log(`Síndicos criados: ${totals.condominios}`);
  log(`Funcionários criados: ${totals.funcionarios}`);
  log(`Inquilinos criados: ${totals.inquilinos}`);
  log(`Gastos criados: ${totals.gastos}`);
  log(`Manutenções criadas: ${totals.manutencoes}`);
  log(`Avisos manuais criados: ${totals.avisos}`);
  log(`Arrecadações mensais definidas: ${totals.arrecadacoes}`);
  log(`Solicitações pendentes deixadas para demonstração: ${totals.solicitacoesPendentes}`);
  log('');
  log('Credenciais de demonstração:');
  log(`Admin: ${ADMIN_CREDENTIALS.email} / ${ADMIN_CREDENTIALS.password}`);
  log('Gestores:');
  for (const item of results) {
    log(`- ${item.condominio.nome}: ${item.gestor.email} / ${item.gestor.password}`);
  }
  log('Síndicos:');
  for (const item of results) {
    log(`- ${item.condominio.nome}: ${item.sindico.email} / ${item.sindico.password}`);
  }
}

async function main() {
  log('Preparando schema mínimo e limpando dataset demo anterior...');
  runSql(schemaSql());
  runSql(cleanupSql());

  log(`Validando acesso HTTP em ${BASE_URL}...`);
  const adminAuth = await login(ADMIN_CREDENTIALS.email, ADMIN_CREDENTIALS.password);
  if (!adminAuth || !adminAuth.token) {
    throw new Error('Login do admin não retornou token. Verifique se o backend já está rodando.');
  }

  const dataset = buildDataset();
  const results = [];

  for (const entry of dataset) {
    log(`Criando dados de demonstração para ${entry.nome}...`);
    results.push(await seedCondominio(adminAuth.token, entry));
  }

  printSummary(results);
}

