function runSql(sql) {
  fs.mkdirSync(DATA_DIR, { recursive: true });

  const h2Jar = findH2Jar();
  const tempSqlFile = path.join(os.tmpdir(), `sigac-seed-${Date.now()}.sql`);
  fs.writeFileSync(tempSqlFile, sql, 'utf8');

  try {
    const result = spawnSync(
      'java',
      [
        '-cp',
        h2Jar,
        'org.h2.tools.RunScript',
        '-url',
        h2Url(),
        '-user',
        'sa',
        '-password',
        '',
        '-script',
        tempSqlFile,
      ],
      {
        cwd: ROOT_DIR,
        encoding: 'utf8',
      },
    );

    if (result.status !== 0) {
      const details = [result.stdout, result.stderr].filter(Boolean).join('\n').trim();
      fail(`Falha ao preparar o banco H2.\n${details}`);
    }
  } finally {
    fs.rmSync(tempSqlFile, { force: true });
  }
}

function escapeHtml(text) {
  return String(text)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

function monthRef(offset) {
  const now = new Date();
  const shifted = new Date(now.getFullYear(), now.getMonth() + offset, 1);
  const year = shifted.getFullYear();
  const month = shifted.getMonth() + 1;

  return {
    year,
    month,
    date(day) {
      return `${year}-${String(month).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
    },
  };
}

